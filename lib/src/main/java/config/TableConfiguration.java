package config;

public class TableConfiguration {
	private String name;
	private String field;
	private int threads;
	private String typename;
	private String key;

	public String getName() {
		return name;
	}

	public String getField() {
		return field;
	}

	public int getThreads() {
		return threads;
	}

	public String getTypename() {
		return typename;
	}

	public String getKey() {
		return key;
	}

}
